DROP TABLE IF EXISTS iso_document_revisions CASCADE;
DROP TABLE IF EXISTS iso_documents CASCADE;

CREATE TABLE iso_documents (
  id            SERIAL PRIMARY KEY,
  category_no   TEXT,
  clause        TEXT,
  title         TEXT NOT NULL,
  version       INT NOT NULL DEFAULT 1,
  dept          TEXT,
  approver      TEXT,
  retention     TEXT NOT NULL CHECK (retention IN
                  ('更新まで','3年','5年','7年','永年','退職まで','退職後2年')),
  storage_link  TEXT,
  standard      TEXT[] NOT NULL DEFAULT '{}',
  note          TEXT,
  is_active     BOOLEAN NOT NULL DEFAULT true,
  sort_order    INT NOT NULL DEFAULT 0,
  created_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at    TIMESTAMPTZ NOT NULL DEFAULT now()
);
CREATE INDEX idx_iso_documents_cat ON iso_documents(category_no);

CREATE TABLE iso_document_revisions (
  id          SERIAL PRIMARY KEY,
  document_id INT NOT NULL REFERENCES iso_documents(id) ON DELETE CASCADE,
  rev_date    DATE,
  rev_content TEXT,
  created_by  TEXT,
  approved_by TEXT,
  created_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);
CREATE INDEX idx_iso_doc_rev_doc ON iso_document_revisions(document_id);

