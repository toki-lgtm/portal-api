DROP TABLE IF EXISTS iso_laws_regulations CASCADE;
CREATE TABLE iso_laws_regulations (
  id                SERIAL PRIMARY KEY,
  category          TEXT NOT NULL,          -- 区分: 品質/環境/労働安全衛生
  law_type          TEXT,                   -- 分類: 法令/その他要求事項
  law_name          TEXT NOT NULL,          -- 法令・規制名
  requirement       TEXT,                   -- 要求事項（親法令は細目名＋根拠条文）
  our_compliance    TEXT,                   -- 当社が遵守すべき事項
  related_equipment TEXT,                   -- 関係設備・材料等
  responsible_dept  TEXT,                   -- 責任部門
  related_records   TEXT,                   -- 関係記録
  compliance_status TEXT,                   -- 順守評価（適合/保留 等・監査時記入）
  sort_order        SMALLINT NOT NULL DEFAULT 0,
  created_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at        TIMESTAMPTZ NOT NULL DEFAULT now()
);
CREATE INDEX idx_iso_laws_category ON iso_laws_regulations(category);
CREATE INDEX idx_iso_laws_sort     ON iso_laws_regulations(sort_order);

