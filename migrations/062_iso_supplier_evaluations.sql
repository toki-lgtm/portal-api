DROP TABLE IF EXISTS iso_supplier_evaluations CASCADE;
CREATE TABLE iso_supplier_evaluations (
  id               SERIAL PRIMARY KEY,
  supplier_name    TEXT NOT NULL,
  trade_category   TEXT NOT NULL CHECK (trade_category IN ('原料供給・保守','請負委託')),
  main_item        TEXT,                 -- 主要取引品目
  transaction_type TEXT DEFAULT '新・継',-- 新規/継続
  criteria         JSONB NOT NULL DEFAULT '{}'::jsonb,  -- {"品質":"○",...}
  result           TEXT CHECK (result IN ('合格','不合格','保留')),
  reason           TEXT,                 -- 選択理由（不合格時等）
  evaluator        TEXT,
  evaluation_date  DATE,
  note             TEXT,
  created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at       TIMESTAMPTZ NOT NULL DEFAULT now()
);
CREATE INDEX idx_iso_supplier_cat ON iso_supplier_evaluations(trade_category);

